using namespace std;
class Solution {
public:
    vector<long long> findPrefixScore(vector<int>& nums) {
        auto const n = nums.size();
        vector<long long >ans;
        ans.reserve(n);
        long long cur = 0;
        int maxi = numeric_limits<int>::min();
        for (auto num: nums) {
          maxi = max(maxi, num);
          cur += maxi+ num;
          ans.emplace_back(cur);
        }
        return ans;
    }
};
