class Solution {
public:
    int minDeletion(vector<int>& nums) {
        int cnt = 0;
        int ans = 0;
        vector<int> stk;
        for (auto n: nums) {
          if (stk.empty())
            cnt++, stk.push_back(n);
          else {
            if (stk.back() == n) {
              ans++;
            }
            else stk.clear();
          }
        }
        return ans + stk.size();
    }
};
