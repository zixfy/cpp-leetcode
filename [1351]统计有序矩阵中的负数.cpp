class Solution {
public:
  int countNegatives(vector<vector<int>> &grid) {
    const auto [m, n] = make_pair(grid.size(), grid[0].size());
    int pivot = n;
    int ans = 0;
    for (int i = 0; i < m; ++i) {
      while (pivot > 0 && grid[i][pivot - 1] < 0)
        pivot--;
      ans += n - pivot;
    }
    return ans;
  }
};
