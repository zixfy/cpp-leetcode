#include "numeric"
#include "string"
#include "vector"
using namespace std;

class Solution {
public:
  int numberOfBeams(vector<string> &bank) {
    int last = 0;
    int ans = 0;
    for (auto &row : bank) {
      auto cnt = std::accumulate(row.begin(), row.end(), 0,
                                 [](int acc, char c) { return acc + c - '0'; });
      if (cnt != 0) {
        ans += last * cnt;
        last = cnt;
      }
    }

    return ans;
  }
};
