class Solution {
public:
    vector<int> circularGameLosers(int n, int k) {
        unordered_set<int> vis;
        auto cur = 0, offset = k;
        vis.insert(cur);
        while (true) {
          cur = (cur + offset) % n;
          offset += k;
          if (vis.count(cur))
            break ;
          vis.insert(cur);

        }
        vector<int> ans;
        for (int i = 0; i < n; ++i)
          if (!vis.count(i))
            ans.emplace_back(i + 1);
        return ans;
    }
};
