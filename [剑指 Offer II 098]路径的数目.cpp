class Solution {
public:

  unsigned int dp[101][101];
  Solution() {
    memset(dp, 0, sizeof(dp));
    for (int i = 1; i <= 100; i++)
      dp[i][1] = dp[1][i] = 1;
    for (int i = 2; i <= 100; i++)
      for (int j = 2; j <= 100; j++) {
        dp[i][j] = dp[i - 1][j] + dp[i][j - 1];
      }
  }
  int uniquePaths(int m, int n) {
    return (int)((this->dp)[m][n]);
  }
};
