/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left),
 * right(right) {}
 * };
 */
class CBTInserter {
private:
  vector<TreeNode *> q;
  int cur_child_cnt = 0, cur_child_max;
  TreeNode *_root;

public:
  ~CBTInserter() {}
  CBTInserter(TreeNode *root) {
    q.push_back(root);
    _root = root;
    cur_child_max = 2;

    while (true) {
      for (auto p : q) {
        int c = (p->left != nullptr) + (p->right != nullptr);
        cur_child_cnt += c;
        if (c < 2)
          break;
      }
      if (cur_child_cnt < cur_child_max)
        break;
      vector<TreeNode *> nxt;
      nxt.reserve(cur_child_max);
      for (auto p : q)
        nxt.push_back(p->left), nxt.push_back(p->right);
      q.swap(nxt);
      cur_child_cnt = 0;
      cur_child_max <<= 1;
    }
  }

  int insert(int v) {
    auto par = q[cur_child_cnt >> 1];
    auto ret = par->val;
    auto & ref = ((cur_child_cnt & 1) ? par->right : par->left);
    ref = new TreeNode(v);

    ++cur_child_cnt;

//    cout << cur_child_cnt << endl;
    if (cur_child_cnt == cur_child_max) {
      vector<TreeNode *> nxt;
      nxt.reserve(cur_child_max);
      for (auto p : q)
        nxt.push_back(p->left), nxt.push_back(p->right);
      q.swap(nxt);
      cur_child_cnt = 0;
      cur_child_max <<= 1;
    }
    return ret;
  }

  TreeNode *get_root() { return _root; }
};

