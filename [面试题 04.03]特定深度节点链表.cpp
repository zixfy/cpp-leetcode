/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
  auto listOfDepth(TreeNode *tree) {
    vector<ListNode *> ans;
    queue<TreeNode *> q;
    if (tree != nullptr)
      q.push(tree);
    while (!q.empty()) {
      int layer_cnt = q.size();
      auto f = [&q]() {
        auto cur = q.front();
        auto r = new ListNode(cur->val);
        q.pop();
        if (cur->left)
          q.push(cur->left);
        if (cur->right)
          q.push(cur->right);
        return r;
      };
      auto head = f();
      auto tail = head;
      for (int i = 1; i < layer_cnt; ++i)
        tail->next = f(), tail = tail->next;
      ans.push_back(head);
    }
    return ans;
  }
};
