#include "vector"
using namespace std;

class Solution {
public:
  bool mergeTriplets(vector<vector<int>> &triplets, vector<int> &target) {
    vector cur{0, 0, 0};

    for (auto &tri : triplets) {
      if (tri[0] > target[0] or tri[1] > target[1] or tri[2] > target[2])
        continue;
      else {
        cur[0] = max(cur[0], tri[0]);
        cur[1] = max(cur[1], tri[1]);
        cur[2] = max(cur[2], tri[2]);
        if (target == cur) {
          return true;
        }
      }
    }
    return false;
  }
};
