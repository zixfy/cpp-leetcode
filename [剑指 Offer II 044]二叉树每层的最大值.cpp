#include <bits/stdc++.h>
using namespace std;
class Solution {
public:
  auto largestValues(TreeNode* root) {
    vector<int> ans;
    function<void(TreeNode*, int)> dfs = [&] (TreeNode* cur, int dep) {
      if (cur == nullptr)
        return;
      if (ans.size() < dep)
        ans.emplace_back(cur->val);
      else {
        ans[dep - 1] = max(ans[dep - 1], cur->val);
      }
      dfs(cur->left, dep + 1);
      dfs(cur->right, dep + 1);
    };
    dfs(root, 1);
    return ans;
  }
};
