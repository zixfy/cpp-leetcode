auto string_cnt_chars(const string_view& s) {
  unordered_map<char, int> ans;
  for (auto c: s)
    ans[c]++;
  return ans;
}

class Solution {
public:
    int percentageLetter(string s, char letter) {
        auto dict = string_cnt_chars(s);
        return dict[letter] * 100 / s.length();
    }
};
