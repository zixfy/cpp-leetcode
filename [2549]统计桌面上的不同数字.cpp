class Solution {
public:
  int distinctIntegers(int n) {
    unordered_set<int> pool{};
    pool.insert(n);
    queue<int> q;
    q.emplace(n);
    while (!q.empty()) {
      auto cur = q.front();
      for (int i = 2; i <= n; ++i)
        if (pool.count(i) == 0 && ((cur - 1) % i == 0))
          pool.insert(i), q.push(i);
      q.pop();
    }
    return pool.size();
  }
};
