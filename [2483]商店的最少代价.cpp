using namespace std;

class Solution {
public:
  int bestClosingTime(string customers) {
    int cur = std::count_if(customers.begin(), customers.end(),
                            [](char c) { return c == 'Y'; });
    int ans = 0, mini = cur;
    int now = 1;
    for (auto c : customers) {
      cur += (c == 'Y') ? -1 : 1;
      if (cur < mini)
        mini = cur, ans = now;
      ++now;
    }

    return ans;
  }
};
